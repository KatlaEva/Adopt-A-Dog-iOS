import UIKit

struct Color {
    static func lightGreen() -> UIColor {
        return UIColor(red: 178/255, green: 231/255, blue: 227/255, alpha: 1.0)
    }
    
    static func creamWhite() -> UIColor {
        return UIColor(red: 255/255, green: 253/255, blue: 247/255, alpha: 1.0)
    }
    
    static func lightPink() -> UIColor {
        return UIColor(red: 254/255, green: 219/255, blue: 233/255, alpha: 1.0)
    }
    
    static func darkGreen() -> UIColor {
        return UIColor(red: 121/255, green: 186/255, blue: 199/255, alpha: 1.0)
    }
}
